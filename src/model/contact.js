const mongoose = require("mongoose");

const model = mongoose.model("contacts", {
    name: {
        type: String,
        required: true
    },
    email: {
        type: String,
        required: true
    },
    phone: {
        type: String,
        required: true
    },
    category: {
        type: String,
        required: true
    }
});

module.exports = model;



